using UnityEngine;
using System.Collections;

namespace FayvitUI
{
    public class FayvitUiEvent : IFayvitUiEvent
    {
        public object[] MySendObjects { get; private set; }

        public UIEventKey Key { get; private set; }

        public FayvitUiEvent(UIEventKey key, params object[] o)
        {
            Key = key;
            MySendObjects = o;
        }
    }
}
